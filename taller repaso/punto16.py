numero = float(input("ingresa un numero : "))

if numero <= 17:
    n = 17 - numero
    print (f"el numero es {numero} y al ser menor que 17 la resta es {n}")
else: 
    n2 = (17 - numero)*2
    print (f"el numero es {numero} y al ser mayo que 17, el resultado es el doble de la diferencia absoluta es {n2}")